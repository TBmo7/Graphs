class Graph:

    """Represent a graph as a dictionary of vertices mapping labels to edges."""
    def __init__(self):
        self.vertices = {}
    
    def __repr__(self):
        return str(self.vertices)

    def add_vertex(self, vertex_id):
        """
        Add a vertex to the graph.
        similar to adding a node
        we need to check if the vertexd id is already in the graph's
        set of vertices
        if it isn't > add it

        """
        
        if vertex_id not in self.vertices:
            self.vertices[vertex_id] = set()

    def add_edge(self, v1, v2):
        """
        Add a directed edge to the graph.
        we're going to check if both vertices exist
        if they do we will add a directional edge
        could possibly turn this into a bidirectional function
        """
        if v1 in self.vertices and v2 in self.vertices:
            self.vertices[v1].add(v2)

    def get_neighbors(self, vertex_id):
        """
        Get all neighbors (edges) of a vertex.
        take in a vertex Id and return vertices
        """
        return self.vertices[vertex_id] if vertex_id in self.vertices else set()        

    def bft(self, starting_vertex):
        """
        Print each vertex in breadth-first order
        beginning from starting_vertex.
        Need to go through the graph breadth first, so layer by layer
        
        when we visit the node, take note of the neighbors, we visit them next, 
        add them to the queue, visit things in the queue
        as we visit a node we can make a visited set(NO DUPLICATES)
        if the node has been visited skip it, else print it
        """
        visited = set()
        queue = deque()
        queue.append(starting_vertex)
        while len(queue) > 0:
            currNode = queue.popleft()
            if currNode not in visited:
                visited.add(currNode)
                print(currNode)
                for neighbor in self.get_neighbors(currNode):
                    queue.append(neighbor)

    def dft(self, starting_vertex):
        """
        Print each vertex in depth-first order
        beginning from starting_vertex.
        """
        stack = deque()
        stack.append(starting_vertex)
        visited = set()
        while len(stack) > 0:
            currNode = stack.pop()
            if currNode not in visited:
                visited.add(currNode)
                print(currNode)
                for neighbor in self.vertices[currNode]:
                    stack.append(neighbor)

